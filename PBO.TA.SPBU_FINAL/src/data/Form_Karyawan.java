    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        
        /* 
            -- Update button function
                -- take ( table name, array field command, primary key) as an argument
        */
        
        String[] arr = { "nik='" + TxtNIK.getText()+"'", "nama='" + TxtNama.getText()+"'", "no_hp='" + TxtNoHP.getText()+"'"};
        int konfirmasi_update = JOptionPane.showConfirmDialog(null, "Apakah Ingin Update Karyawan " + TxtIDTerpilih.getText());
        
        if (konfirmasi_update == 0){
            JOptionPane.showMessageDialog(this, "karyawan " + TxtIDTerpilih.getText() + " Terupdate");
            function.update_table(nama_table, arr, "id_karyawan='"+TxtIDTerpilih.getText()+"'");
            function.load_table(tblmodel, this.arr, nama_table);
            resetInput();
        }  
       
    }//GEN-LAST:event_btnEditActionPerformed

    private void BtnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnDeleteActionPerformed
        
        /* 
        Delete Function usage
            -- this function will pass arguments to tools function
                -- delete argument must filled with ( table_name, field + primary key ) 
        */
        
        int konfirmasi_delete = JOptionPane.showConfirmDialog(this, "Id Karyawan " + TxtIDTerpilih.getText() + " Terhapus");
        if (konfirmasi_delete == 0){
                
                JOptionPane.showMessageDialog(this, "Id Karyawan " + TxtIDTerpilih.getText() + " Terhapus");
                function.delete_table(nama_table, "id_karyawan=" + TxtIDTerpilih.getText());
                function.load_table(tblmodel, arr, nama_table);
                resetInput();
                
            }
        
    }//GEN-LAST:event_BtnDeleteActionPerformed

